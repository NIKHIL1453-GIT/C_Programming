//Increment
#include<stdio.h>
int main()
{
	int a=10;
	int b=a;
	printf("\nvalue of a before pre increment = %d",a);
	printf("\nvalue of a after pre increment = %d",++a);
	printf("\nvalue of b before post increment = %d",b++);
	printf("\nvalue of b after post increment = %d",b);
	
	return 0;
}
