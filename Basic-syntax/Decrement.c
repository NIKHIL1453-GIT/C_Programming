//Decrement
#include<stdio.h>
int main()
{
	int a=10;
	int b=a;
	printf("\nvalue of a before pre decrement = %d",a);
	printf("\nvalue of a after pre decrement = %d",--a);
	printf("\nvalue of b before post decrement = %d",b--);
	printf("\nvalue of b after post decrement = %d",b);
	
	return 0;
}
